        animation=xerolink,
        caption=f"{XEXO}ğŸ§ ğ—«ğ—²ğ—¿ğ—¼ğ—»ğ—¼ğ—¶ğ—± ğ˜‚ğ˜€ğ—²ğ—¿ğ—¯ğ—¼ğ˜ ğ—µğ—®ğ˜€ ğ˜€ğ˜ğ—¼ğ—½ğ—½ğ—²ğ—± ğ—½ğ—¹ğ—®ğ˜†ğ—¶ğ—»ğ—´ ğ—ºğ˜‚ğ˜€ğ—¶ğ—° ğ—¶ğ—»\n**{chat.title}**",
        reply_markup = MIB

async def Xero_Singing(_, __, XS: XeroSpeak):
    group_call = XePlay.group_call
    if not (group_call and group_call.is_connected):
        return False
    chat_id = int("-100" + str(group_call.full_chat.id))
    if XS.chat.id == chat_id:
        return True
    return False

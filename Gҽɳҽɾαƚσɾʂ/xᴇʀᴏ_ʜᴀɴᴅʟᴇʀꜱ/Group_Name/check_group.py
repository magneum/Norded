@Client.on_message(
filters.group
& filters.chat(CHAT_ID)
& filters.command("group", prefixes="/"))
async def list_voice_chat(client, m: Message):
    group_call = XePlay.group_call
    if group_call and group_call.is_connected:
        chat_id = int("-100" + str(group_call.full_chat.id))
        chat = await client.get_chat(chat_id)
        await client.send_animation(
            animation=xerolink,
            duration=10,
            chat_id=LOGGER_ID,
            caption=f"{XEXO}ğŸ§ (IN_GROUP)\nXeronoid Userbot has been plugged in the voice chat of:**{chat.title}**"
        )
        group_info = await m.reply_animation(
            animation=xerolink,
            caption=f"{XEXO}ğŸ§ Userbot is plugged in the voice chat of **{chat.title}**",
            reply_markup = MIB    
        )
       
    else:
        await client.send_animation(
            animation=xerolink,
            duration=10,
            chat_id=LOGGER_ID,
            caption=f"{XEXO}ğŸ§ (IN_GROUP)\nXeronoid Userbot has not been plugged yet"
        )
        
        group_info = await m.reply_animation(
            animation=xerolink,
            caption=f"{XEXO}ğŸ§ Userbot is not plugged in any voice chat yet",
            reply_markup = MIB
            )
        

    # delete help info in group chats to keep it clean no matter what 
    await xeronoid_help_purge(
        (group_info,m),
        GROUP_REMOVER)
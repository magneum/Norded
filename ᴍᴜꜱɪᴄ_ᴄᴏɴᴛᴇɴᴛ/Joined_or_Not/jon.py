async def XePlay_Texter(text):
    Xero_Voixe = XePlay.Xero_Voixe
    client = Xero_Voixe.client
    chat_id = XePlay.chat_id
    message = await client.send_message(
        chat_id,
        text,
        disable_web_page_preview=True,
        disable_notification=True
    )
    return message